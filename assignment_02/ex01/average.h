#ifndef AVERAGE_H
#define AVERAGE_H

#include<stdio.h>


/**
 * Calculate the average value of an integer array.
 *
 * This function sums all elements of the array and divides
 * the total by the number of elements to compute the average.
 *
 * @param arr  Pointer to the first element of the integer array.
 * @param size Number of elements in the array.
 *
 * @return The average value of the array as a float.
 */
float average(int arr[],size_t size);



#endif